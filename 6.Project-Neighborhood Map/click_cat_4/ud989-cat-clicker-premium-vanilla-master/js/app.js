/* ======= Octopus ======= */

var octopus = {

    init: function() {
        // set our current cat to the first one in the list
        model.currentCat = model.cats[0];

        // tell our views to initialize
        catListView.init();
        catView.init();
    },

    getCurrentCat: function() {
        return model.currentCat;
    },

    getCats: function() {
        return model.cats;
    },

    // set the currently-selected cat to the object passed in
    setCurrentCat: function(cat) {
        model.currentCat = cat;
    },

    // increments the counter for the currently-selected cat
    incrementCounter: function() {
        model.currentCat.clickCount++;
        catView.render();
    },

    modifyCurrentCatInfo: function(cat) {
        model.currentCat = cat;
        catView.render();
    }
};


/* ======= View ======= */

var catView = {

    init: function() {
        // store pointers to our DOM elements for easy access later
        this.catElem = document.getElementById('cat');
        this.catNameElem = document.getElementById('cat-name');
        this.catImageElem = document.getElementById('cat-img');
        this.countElem = document.getElementById('cat-count');

        this.catNameInElem = document.getElementById('cat-name-input');
        this.catImageInElem = document.getElementById('cat-img-url-input');
        this.countInElem = document.getElementById('cat-click-input');

        // on click, increment the current cat's counter
        this.catImageElem.addEventListener('click', function(){
            octopus.incrementCounter();
        });

        // render this view (update the DOM elements with the right values)
        this.render();
        this.catInput();
    },

    render: function() {
        // update the DOM elements with values from the current cat
        var currentCat = octopus.getCurrentCat();
        this.countElem.textContent = currentCat.clickCount;
        this.catNameElem.textContent = currentCat.name;
        this.catImageElem.src = currentCat.imgSrc;
    },

    catInput: function() {
        var currentCat = octopus.getCurrentCat();
        this.countInElem.value = currentCat.clickCount;
        this.catNameInElem.value = currentCat.name;
        this.catImageInElem.value = currentCat.imgSrc;
    },

    modifyCatInfo: function() {
        var currentCat = octopus.getCurrentCat();
        currentCat.clickCount = this.countInElem.value;
        currentCat.name = this.catNameInElem.value;
        currentCat.imgSrc = this.catImageInElem.value;
        octopus.modifyCurrentCatInfo(currentCat);
    }
};

var catListView = {

    init: function() {
        // store the DOM element for easy access later
        this.catListElem = document.getElementById('cat-list');
        this.buttonAdmin = document.getElementById('user');
        this.buttonSave = document.getElementById('save');
        this.buttonCancel = document.getElementById('cancel');

        // render this view (update the DOM elements with the right values)
        this.render();
    },

    render: function() {
        var cat, elem, i;
        // get the cats we'll be rendering from the octopus
        var cats = octopus.getCats();

        // empty the cat list
        this.catListElem.innerHTML = '';

        // loop over the cats
        for (i = 0; i < cats.length; i++) {
            // this is the cat we're currently looping over
            cat = cats[i];

            // make a new cat list item and set its text
            elem = document.createElement('li');
            elem.textContent = cat.name;

            // on click, setCurrentCat and render the catView
            // (this uses our closure-in-a-loop trick to connect the value
            //  of the cat variable to the click event function)
            elem.addEventListener('click', (function(catCopy) {
                return function() {
                    octopus.setCurrentCat(catCopy);
                    catView.render();
                    catView.catInput();
                };
            })(cat));

            // finally, add the element to the list
            this.catListElem.appendChild(elem);
        }

        this.buttonAdmin.addEventListener('click', function() {
            
        });

        this.buttonSave.addEventListener('click', function() {
            
            
            catView.modifyCatInfo();
            alert("ok");
        });

        this.buttonCancel.addEventListener('click', function() {
        });

    }
};

// make it go!
octopus.init();
